package Loops;

public class For_Demo {

	public static void main(String[] args) {
		
		for(int i=1;i<=6;i++)                      //i=1  1<=6  group4
		{
			System.out.println("Group4");          //i=2  2<=6   group4
			                                       //i=3  3<=6    
		}
		for(int i=5;i>=1;i--)
		{
			System.out.println("pradnya");
		}

	}

}
